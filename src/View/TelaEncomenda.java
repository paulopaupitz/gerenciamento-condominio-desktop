    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        
        int linhaSelecionada = tblEncomenda.getSelectedRow();
            if(linhaSelecionada == -1){
                JOptionPane.showMessageDialog(this,"Por favor, selecione uma linha");
            }
            else{
                DefaultTableModel tabelaEncomenda =
                        (DefaultTableModel) tblEncomenda.getModel();
                tabelaEncomenda.removeRow(linhaSelecionada);
                JOptionPane.showMessageDialog(this,"Encomenda Excluída");
            }
    }//GEN-LAST:event_btExcluirActionPerformed

