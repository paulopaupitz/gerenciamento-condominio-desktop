    private void btnSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSActionPerformed
        //RECUPERAR DADOS PREENCHIDOS NO FORMULARIO
        String numero = txtNum.getText();
        String morador = txtMorador.getText();
        
        DefaultTableModel tabelaEncomenda = 
            (DefaultTableModel) tblEncomenda.getModel();
        
        Object[] novaEncomenda = new Object[]{
            morador,
            numero
        };
        
        tabelaEncomenda.addRow(novaEncomenda);
        txtMorador.setText("");
        txtNum.setText("");
    }//GEN-LAST:event_btnSActionPerformed

